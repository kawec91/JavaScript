function listeBearbeiten(liste) {
  let result = ``;

  for (let i = 0; i < liste.length; i++) {
    if (liste[i].content !== undefined) {
      if (liste[i].element === "img") {
        result += `<${liste[i].element} src="${liste[i].content}">`;
      } else if (liste[i].element === "a") {
        result += `<${liste[i].element} href="${liste[i].link}">${liste[i].content}</a>`;
      } else if (liste[i].element === "hr" || liste[i].element === "br") {
        result += `<${liste[i].element} />`;
      } else if (liste[i].element === "p") {
        result += `<${liste[i].element}>${liste[i].content}</${liste[i].element}>`;
      } else if (liste[i].element === "input") {
        result += `<${liste[i].element} type="${liste[i].type}" placeholder="${liste[i].content}">`;
      } else if (liste[i].element === "button") {
        result += `<${liste[i].element} onclick="${liste[i].func}">${liste[i].content}</${liste[i].element}>`;
      } else {
        result += `<${liste[i].element}>${listeBearbeiten(liste[i].content)}</${
          liste[i].element
        }>`;
      }
    }
    console.log(liste[i].content);
  }
  return result;
}

function app(liste) {
  const conWithBody = document.getElementById("body");

  conWithBody.innerHTML = listeBearbeiten(liste);
}
app(jsonObiektListe);
